<template>
    <transition
        name="fade"
        appear
        @before-enter="beforeEnter"
        @enter="enter"
        @leave="leave"
      >
    <div
      class="portal-tooltip"
      role="tooltip"
      data-test="portal-tooltip"
      :style="tooltipPosition"
    >
      <div
        class="portal-tooltip__header"
      >
        <icon-button
          icon="x"
          class="portal-tooltip__close-icon"
          :aria-label-prop="CLOSE_TOOLTIP"
          @click="closeToolTip()"
        />
      </div>

      <!-- eslint-disable vue/no-v-html -->
      <div
        v-if="description"
        :id="ariaId"
        class="portal-tooltip__description"
        v-html="description"
      />
    <!-- eslint-enable vue/no-v-html -->
    </div>
      </transition>

</template>

<script lang="ts">
import { defineComponent, PropType } from 'vue';
import _ from '@/jsHelper/translate';

import IconButton from '@/components/globals/IconButton.vue';

export default defineComponent({
  name: 'PortalToolTip',
  components: {
    IconButton,
  },
  props: {
    backgroundColor: {
      type: String,
      default: '',
    },
    description: {
      type: String,
      default: '',
    },
    ariaId: {
      type: String,
      default: '',
    },
    position: {
      type: Object as PropType<Record<string, number>>,
      required: true,
    },
  },
  computed: {
    CLOSE_TOOLTIP(): string {
      return _('Close Tooltip');
    },
    tooltipPosition(): string {
      return `left:${this.position.x}px;`;
    },
  },
  mounted() {
    console.log('PortalToolTip', this);
  },
  methods: {
    closeToolTip() {
      this.$store.dispatch('tooltip/unsetTooltip');
    },
    beforeEnter(el) {
      el.style.top = `${this.position.bottom}px`;
      el.style.transition = 'all 0.5s ease-out';
    },
    enter(el, done) {
      // setTimeout(() => {
      //   el.style.top = `${this.position.bottom - 5}px`;
      // }, 200);
    },
    leave(el, done) {
      el.style.top = `${this.position.bottom}px`;
      el.style.transition = 'all 0.5s ease-out';
    },
  },
});
</script>

