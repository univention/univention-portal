<template>
  <div class="radio-box">
    <label
      v-for="option in options"
      :key="option"
      :for="`${name}--${option.id}`"
      class="radio-box__label"
    >
      <input
        type="radio"
        name="radio-input"
        :value="option.id"
        :id="`${name}--${option.id}`"
        class="radio-box__input"
        @change="$emit('update:modelValue', option.id)"
      >
      {{ option.label }}
    </label>
  </div>
</template>

<script lang="ts">
import { defineComponent } from 'vue';
import { isValid } from '@/jsHelper/forms';

export default defineComponent({
  name: 'RadioBox',
  props: {
    modelValue: {
      type: String,
      required: true,
    },
    invalidMessage: {
      type: String,
      default: '',
    },
    options: {
      type: Array,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
  },
  emits: ['update:modelValue'],
  computed: {
    invalid(): boolean {
      return !isValid({
        type: 'RadioBox',
        invalidMessage: this.invalidMessage,
      });
    },
  },
});
</script>
<style lang="stylus">
.radio-box
  &__label
    display: flex
    align-items: center
</style>
